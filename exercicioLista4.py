#Exercício 4: Potência
#Pedir dois números ao usuário: base e expoente
numero = int(input("Digite um numero:"))
expoente = int(input("Digite o expoente:"))
#Calcular a potência (base elevada ao expoente)
dois= numero*expoente
#Mostrar o resultado da operação
print(f"A potencia de {numero} é {dois}")